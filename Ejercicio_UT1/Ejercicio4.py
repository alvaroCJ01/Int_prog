n1=float(input("Introduce el primer número a calcular: "))
n2=float(input("Introduce el segundo número a calcular: "))

suma=n1+n2
resta=n1-n2
multiplicar=n1*n2
division=n1/n2

print(f"{n1} + {n2} = {suma}")
print(f"{n1} - {n2} = {resta}")
print(f"{n1} * {n2} = {multiplicar}")
print(f"{n1} / {n2} = {division}")